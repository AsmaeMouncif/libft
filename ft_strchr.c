#include "libft.h"

char	*ft_strchr(const char *s, int ch)
{
	int	i;
	unsigned char c;

	c = (unsigned char)ch;
	i = 0;
	while (s[i] != '\0')
	{
		if ((unsigned char)s[i] == c)
		{
			return ((char *)&s[i]);
		}
		i++;
	}
	if (c == '\0')
		return ((char *)&s[i]);
	return (NULL);
}
