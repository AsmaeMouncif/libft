#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		l;
	char	*p;
	int		i;

	l = ft_strlen(s);
	p = malloc(l + 1);
	if (p == NULL)
	{
		return (NULL);
	}
	i = 0;
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
